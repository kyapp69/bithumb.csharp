    /// <summary>
    /// bithumb 거래소 회원 지갑 정보
    /// </summary>
    public class UserBalance : ApiResult<UserBalanceData>
    {
        public UserBalance()
        {
            this.data = new UserBalanceData();
        }

        /// <summary>
        /// 사용 가능 QTY
        /// </summary>
        public decimal available_qty(string coin_name)
        {
            var _result = 0.0m;

            switch (coin_name.ToUpper())
            {
                case "KRW":
                    _result = this.data.available_krw;
                    break;
                case "BTC":
                    _result = this.data.available_btc;
                    break;
                case "ETH":
                    _result = this.data.available_eth;
                    break;
                case "DASH":
                    _result = this.data.available_dash;
                    break;
                case "LTC":
                    _result = this.data.available_ltc;
                    break;
                case "ETC":
                    _result = this.data.available_etc;
                    break;
                case "XRP":
                    _result = this.data.available_xrp;
                    break;
                case "BCH":
                    _result = this.data.available_bch;
                    break;
                case "XMR":
                    _result = this.data.available_xmr;
                    break;
                case "ZEC":
                    _result = this.data.available_zec;
                    break;
                case "QTUM":
                    _result = this.data.available_qtum;
                    break;
                case "BTG":
                    _result = this.data.available_btg;
                    break;
                case "EOS":
                    _result = this.data.available_eos;
                    break;
                case "ICX":
                    _result = this.data.available_icx;
                    break;
            }

            return _result;
        }
    }
